/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
using namespace std;
#include<stack>
class BSTIterator {
private:
    stack<int> s;
    void load(stack<int>&s,TreeNode* root){
        if(!root) return;
        load(s,root->right);
        s.push(root->val);
        load(s,root->left);
    }
public:
    BSTIterator(TreeNode* root) {
        load(s,root);
    }
    
    /** @return the next smallest number */
    int next() {
        int n = s.top();
        s.pop();
        return n;
    }
    
    /** @return whether we have a next smallest number */
    bool hasNext() {
        return !s.empty();
    }
};

